public class DeviceInfo {
	private String name;
	private int id;
	private String param;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

    

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getParam() {
		return param;
	}

	public void setParam(String param) {
		this.param = param;
	}

}
